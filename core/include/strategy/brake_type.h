#pragma once

// Тип границы ребра поля (см. Field::brake_types_). Каждый тип, кроме Off,
// имеет собственный профиль торможения (wall_limit + decel_k),
//   Off           — Segment::apply на ребре не вызывается (виртуальная
//                   граница без какого-либо торможения).
//   Wall          — физический бортик поля (внешний прямоугольник
//                   x=12/170, y=12/231). Собственный wall_limit позволяет
//                   двигать периметр независимо (рампы у бортика на полях
//                   соревнований).
//   VirtualNormal — виртуальная граница с обычным торможением (разметка
//                   ворот, боковины keeper-исключения).
//   VirtualLow    — виртуальная граница с ослабленным торможением (верх
//                   keeper-исключения у нападающего: не мешает погоне за
//                   врагом, но push-out по-прежнему не пускает в зону).
enum class BrakeType { Off, Wall, VirtualNormal, VirtualLow };
