#include "media/r2d2.h"

// Параметрические свипы для piezo-buzzer'а. Числа подобраны эмпирически в
// диапазоне ~300-2600 Hz, который хорошо передаётся пьезой и узнаваем на слух
// как "robot beep". Длительности — короткие (<1 с суммарно), чтобы не
// блокировать реакцию на следующее событие.
//
// Формат: {f_start, f_end, ms}.  f_start == 0 → пауза.

const std::vector<Chirp> r2d2_startup = {
    {500, 2400, 250},   // восходящий свип "оживаю"
    {0, 0, 40},         // короткая пауза
    {1700, 2200, 50},   // warble 1
    {2200, 1700, 50},   // warble 2
    {1700, 2200, 50},   // warble 3
    {2200, 1700, 50},   // warble 4
    {0, 0, 30},         // пауза
    {1500, 1500, 80},   // короткий хвостовой тон
};

const std::vector<Chirp> r2d2_pause_in = {
    {2200, 350, 450},   // длинный нисходящий свип "засыпаю"
};

const std::vector<Chirp> r2d2_pause_out = {
    {500, 2400, 300},   // восходящий свип "просыпаюсь"
    {1800, 2300, 30},   // короткий warble в конце
    {2300, 1800, 30},
};

// "Жалобный" сигнал низкого заряда: два коротких нисходящих "ой-ой" в
// средне-низком регистре, затем долгий проседающий тон. Намеренно не
// похоже на startup (восходящий) и pause_in (длинный нисходящий) —
// чтобы оператор узнавал именно "батарея".
const std::vector<Chirp> r2d2_low_battery = {
    {1300, 700, 140},   // "ой" 1
    {0, 0, 60},         // пауза
    {1300, 700, 140},   // "ой" 2
    {0, 0, 60},         // пауза
    {900, 450, 320},    // долгий проседающий хвост
};
